#include "3-calc.h"

/**
 *op_add-add
 *Return:int
 *@a:a
 *@b:b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub-sub
 *Return:int
 *@a:a
 *@b:b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul-mul
 *Return:int
 *@a:a
 *@b:b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div-div
 *Return:int
 *@a:a
 *@b:b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod-mod
 *Return:int
 *@a:a
 *@b:b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
